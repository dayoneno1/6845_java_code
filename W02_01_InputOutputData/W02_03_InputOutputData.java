public class W02_03_InputOutputData {
    public static void main(String[] args) {
        int side = 15; //กำหนดความยาวด้านของสี่เหลี่ยมจัตุรัส
        int area = side * side; //คำนวณพื้นที่โดยการยกกำลังสองของความยาวด้าน
        //แสดงผลลัพธ์
        System.out.println("พื้นที่สี่เหลี่ยมจัตุรัสที่มีด้าน = "+side+" คือ " + area);
    }
}
